// Loops
void loops() {
  // For loop
  for (int i = 1; i <= 5; i++) {
    print('Dart is awesome! $i');
  }

  // While loop
  int j = 1;
  while (j <= 5) {
    print('Dart is cool! $j');
    j++;
  }

  // Do-while loop
  int k = 1;
  do {
    print('Dart is fun! $k');
    k++;
  } while (k <= 5);
}
